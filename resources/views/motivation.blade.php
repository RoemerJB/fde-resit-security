        <h3>What is motivation?</h3>
        <p>
            Motivation is what drives us to take action.
            To be motivated means to have a goal to work towards. Take studying, for example,
            the main motivation for it is to get a degree. To be exact, that degree is a form of extrinsic
            motivation.<br><br>
            What are the types of motivation? Depending on where you look, motivation
            can be split into two types: intrinsic motivation and extrinsic motivation.<br><br>
            Intrinsic motivation is the sort of motivation which comes from our own body.
            Meaning factors like hunger, sleep, desires and curiosity. But also fear and
            and the psychological need for approval and acceptance are intrinsic factors.
            A few examples: hunger drives us to go eat, tiredness drives us to sleep and so on.<br>
            <br> Now, extrinsic motivation is every external factor that drives us.
            A lot of aspects of our current society are based on this type of motivation.
            As mentioned earlier, studying to get a degree and possibly a good job, are examples
            of extrinsic motivation. Studying to get that dream job you've always wanted, on the
            other hand, is intrinsic. And that latter is the optimal drive for studying.
        </p>
    </article>
    <article id="tedTalk">
        <h3>Motivation according to Dan Pink</h3>
        <p>
            What is motivation?<br>
            According to Dan Pink, motivation is what drives people to perform.
            <br><br>
            What determines it?<br>
            Good incentives can determine someone's motivation. Incentives work especially well
            in extrinsic motivation settings, where you'd reward someone who performs well. But
            as studies have shown, intrinsic motivation through autonomy works best in the 21st
            century.
            <br><br>
            How can you manage it?<br>
            In his Tedtalk Dan Pink doesn't talk a lot about managing your own motivation.
            But according to his views on the studies he described, management of one's own
            motivation can be achieved through finding the right incentives.
        </p>
    </article>
</section>
