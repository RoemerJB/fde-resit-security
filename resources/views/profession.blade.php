        <h2 class="heading">Eljakim</h2>
        <p>
            Eljakim IT is a software company whose projects hold a meaning to society. They develop software
            for the government and non-profit organizations within the healthcare and education sectors.
            The company has a few standard applications, but they still value innotvative and challenging
            projects. They have about 30 people working for them, which makes it a relatively small company,
            but these people are very ambitious and creative. Employees at Eljakim IT get a lot of independence
            within their teams. In these teams people share their experiences and together they take on large and
            challenging projects. In 2018, Eljakim IT was the first Dutch company to be certified according to the
            NEN7510:2017 and ISO 27001:2013 norms, which offer protocols for information security meant for Dutch
            healthcare institutions. <br><br> IT roles within the company are: <br>
            There are a lot of different IT roles within the company, mainly software developer. But Eljakim IT
            encourages their employees to keep developing their skills and learning new ones.
