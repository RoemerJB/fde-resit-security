            and makes sure the client's systems stay up to date and run smoothly. They help their
            clients with problems within the field of ICT and advise on what solutions could work
            best. There motto is: <em>'Initiating, Consistant, Transparant... that is ICT we want you
                to experience.'</em> Some IT roles within their company are: <br><br>
            <em>PHP programmer</em>: As PHP programmer you work to automate and optimize both the
            company's own websites and those of their clients with your team. <br>
            <em>Network administrator</em>: Your job is to maintain A&A Capacity's and their clients'
            servers. You also assist in the prevention of server malfunctions and document processes
            in information systems. <br>
            <em>Creative front-end developer</em>: You work together in a small team to design a webpage,
            for example, that adhere to the wishes of the company's clients. After the team has come up
            with a design, you get to realizing it. Afterwards, as a front-end developer, you are also
            responsible for maintaining websites made for clients. <br>
            <em>Lead software developer</em>: You're an all-round software developer and your work mainly
            consists of building complex web apps and services, mobile apps and system integrations.
            As a lead developer, you also constantly immerse yourself in new techniques and you review
            the work of collegues. <br>
            <em>App management</em>: You are responsible for the software A&A Capacity uses for its clients. <br><br>
            Technology used within A&A Capacity: <br>
            Web development languages like <em>HTML</em>, <em>CSS</em> and <em>JavaScript</em>. <br>
            <em>Wordpress</em> is used for building dynamic websites, since it's an open-source website
            creation platform using PHP and a MySQL database. <br>
            <em>A CMS-system</em>, which is a computer software used to create and modify digital content.
