    <article id="ns">
        <h2 class="heading">NS</h2>
        <p>
            The Dutch Railways are the ones responsible for the railways in the country. The NS strives to make
            their travellers' trips as pleasant and sustainable as possible from door to door. For the past years
            they've been engaging in fossil-free, circular and green enterprise, contributing to the UN's Sustainable
            Development Goals. Besides this they also implement a lot of digital services, from websites to mobile apps.
            IT roles within the NS are: <br><br>
            <em>Mendix developer</em>: As mendix developer you'll work with a team on diverse projects within the NS.
            Your team is a part of the department for Low Code projects within the Competence Center Mobile. You'll work
            in complex environments, use state-of-the-art technologies and work for high demanding end-users. The work as
            mendix dev will be varying and challenging, one day you will work on building new and innovative solutions
            for NS Commerce and Material & Maintenance, another day you'll further develop the how-is-your-journey function
            within the NS-app. <br>
            <em>Administrator</em>: As sort of manager you allow the NS to function in the most optimal way possible. You work
            with new cloud technologies and provide support to the Data & Analytics team. <br>
            <em>Lead back-end developer (Java)</em>: You work on Adjustment application Materials (B@M). With this the department
            transport control controls the use of material for ~7.000 train rides a day. This means your work contributes to the
            functioning of the trains themselves, not only when they are on schedule, but especially when there are disturbances. <br>
            <em>Data engineer</em>: As data engineer your role is to maintain availability of data pipelines within the NS. It's your
            job to make sure this data is reliable and that it is processed in a scalable and efficient manner. You work whithin a
            Azure cloud environment in which all streaming datasources are decrypted for further analysis. <br>
            <em>Tester</em>: As tester you have to test the stability and quality of the NS systems. Besides this you also test
            for security issues and build and run automated tests with your team. <br><br>
            Technology used within the NS: <br>
            They use Scrum and Agile methods in their work. <br>
            They use multiple cloud environments, of which Azure is the most used.
        </p>
