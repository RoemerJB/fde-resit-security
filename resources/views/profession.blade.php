        <h2 class="heading">The police force</h2>
        <p>
            Cybersecurity is getting more and more relevant. Now that cyber crimes
            are a thing and virtual attacks by terrorists is reality, digital protection
            is becoming very important. And of course the police is doing everything
            they can to keep up to date with the digitalization of these threats. <br>
            Within the police force, there are multiple departments dealing with IT. and
            all of them have different types of jobs. Examples of these jobs are: <br><br>
            <em>Cybercrime specialist</em>: You will work in a dynamic team with other cyber
            specialists. There's a huge variety in how your days may look. One day you may be
            assisting in an investigation into a botnet, while another day you'll work on
            stopping a DDoS attack. <br><br>
            <em>ETL developer</em>: As a Extraction, Transformation and Load developer within
            the BI department you will develop crucial police systems that help your collegues
            both in the field and with coming up with the right solutions for the job at hand. <br><br>
            <em>Developer tracking applications</em>: You will design new applications and adapt
            already existing software within the field of tracking digital evidence. <br><br>
            <em>Developer Appworks</em>: You wil be a part of the DevOps teams within the police
            force, which means you will work on developing and maintaining both Appworks and
            Documentum. Besides this you will also identify product risks and advise on the
            implementation of new standards and protocols. <br><br>
            <em>DevOps engineer</em>: Your task will be to install, configure, manage, test
            and monitor applications and products your collegues have to use and make sure
            these can function without failure. <br><br>
            Technologies used within the police force are: <br>
            <em>Python</em> and <em>Linux</em> are both programming languages and the main
            ones used on the force. They use <em>scrum</em> as a method for their software development.
            It is a framework which allows you to flexibly develop software projects.
        </p>
